발굴된 기록을 보면, 성벽의 높이가 30미터에 달했다고 합니다. 게다가 성벽 위는 전차 네 대가 나란히 달릴 수 있을 만큼 두꺼웠습니다. 수백 개의 망대가 서 있었고, 둘레에는 깊은 해자가 파여 있었습니다. 당시 세계에서 가장 견고하고 웅장한 도시였습니다.
그런데 하나님은 그 모든 것이 첫 무화과 같다고 하십니다. 살짝 흔들기만 하면, 바람만 스쳐도 떨어진다고 하십니다.
여기서 우리가 만나는 것은 세상의 시선과 하나님의 시선 사이의 거리입니다. 세상이 견고하다고 보는 것을, 하나님은 익어 떨어질 무화과로 보십니다. 우리가 크고 안전하고 대단하다고 느끼는 것이, 하나님 보시기에는 한순간에 떨어질 것일 수 있습니다.
13절은 더 충격적입니다.

"보아라. 네 한가운데 있는 군대는 여자들뿐이다!"
여기서 ''여자들''은 히브리어 ''나쉼''(נָשִׁים)입니다. 이것은 여성을 비하하는 표현이 아니라, 한때 용맹했던 전사들이 극도의 공포에 질려 싸울 힘을 완전히 잃어버린 상태를 그리는 것입니다.
세상에서 가장 잔혹하고 강력하다고 자랑하던 앗수르 군대. 그들이 하나님의 심판 앞에서는 무릎이 풀려 버립니다.

14절에서 하나님의 음성이 들립니다.

"포위에 대비해서 물을 길어 두고 네 요새를 강화시켜라! 진흙탕에 가서 진흙을 뭉쳐서 벽돌을 만들어라!"

언뜻 들으면 하나님이 도와주시는 것 같습니다. 그러나 이것은 비꼬는 말입니다.
''어디 한번 네 힘으로 막아 보아라. 물도 길어 보고, 성벽도 보강해 보고, 벽돌도 만들어 보아라.''
그리고 15절이 차갑게 결론을 내립니다.

"그곳에서 불이 너를 삼킬 것이고 칼이 너를 벨 것이다."

아무리 준비해도 소용없다는 것입니다. 하나님이 심판하기로 작정하신 자리에서는, 모든 방어가 무의미합니다.

16절과 17절에는 메뚜기 비유가 이어집니다. 특히 16절을 눈여겨봐야 합니다.

"네가 네 상인들의 수를 늘려 하늘의 별들보다 많게 했으나."

''하늘의 별처럼 많게.''
이 표현은 우연이 아닙니다. 창세기에서 하나님이 아브라함에게 하신 약속을 흉내 낸 것입니다. 하나님은 아브라함에게 "네 자손이 하늘의 별처럼 많아지리라" 약속하셨습니다.
그런데 니느웨가 그 약속을 흉내 내듯, 자기 상인들을 별처럼 늘렸습니다.
하나님만이 하실 수 있는 일을, 자기 힘으로 이루려 한 것입니다.

결과가 어땠을까요?

"느치 떼처럼 허물을 벗고 나서 날아갈 것이다."

별처럼 많던 자들이, 메뚜기처럼 한순간에 사라지고 말았습니다.

오늘 본문을 보면서 우리가 기억해야 할 것은, 내가 견고하다고 믿는 것이 정말 견고한가 하는 점입니다.
30미터 성벽을 자랑하던 니느웨가 첫 무화과처럼 떨어졌습니다. 내가 의지하는 것이 하나님 보시기에도 견고한 것인지, 아니면 흔들면 떨어질 무화과인지 돌아보게 됩니다.

6월에는 요나서와 나훔서를 살펴 보았습니다.

두 책은 같은 도시, 니느웨를 두고 정반대의 결말을 보여주고 있습니다.

요나서는 회개하면 살리시는 하나님을, 나훔서는 끝까지 회개하지 않으면 멸하시는 하나님을 보여 줍니다.

그리고 그 니느웨는 바로 우리의 모습입니다.

말씀 위에 든든히 서 있다면, 우리가 지을 성은 무화과처럼 떨어지는 성이 아니라 반석 위에 세워진 평안의 성이 될 것입니다.
세상이 견고하다 자랑하는 성벽이 아니라, 하나님을 의지하는 낮고 단단한 기초 위에 우리의 삶을 세워가시기를 바랍니다.

